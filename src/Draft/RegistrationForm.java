    private void btnRegFormSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegFormSubmitActionPerformed
        // TODO add your handling code here:
        Registration regis = new Registration();
        regis.setId(Integer.valueOf(regFormId.getText()));
        regis.setFirstName(regFormFirstName.getText());
        regis.setLastName(regFormLastName.getText());
        
        regMale.setActionCommand("Male");
        regFemale.setActionCommand("Female");
        btnGroupGender.setSelected(null, rootPaneCheckingEnabled);
        //String gender = btnGroupGender.getSelection().getActionCommand();
        regis.setGender(btnGroupGender.getSelection().getActionCommand());
        
        Date dob = new Date(regFormDoB.getDate().getTime());
        regis.setDateOfBirth(dob);
        
        regis.setPresentAddress(regFormPresentAddress.getText());
        regis.setPermanentAddress(regFormPermanentAddress.getText());
        regis.setEmail(regFormEmail.getText());
        regis.setPhone(Integer.valueOf(regFormPhone.getText()));
        regis.setDepartmentId(Integer.valueOf(regFormDepartmentId.getText()));
        regis.setDepartmentName(regFormDepartmentName.getText());
        regis.setBatchId(Integer.valueOf(regFormBatchId.getText()));
        
        String position = regFormPosition.getSelectedItem().toString();
        
        regis.setRankId(Integer.valueOf(regFormRankId.getText()));
        regis.setPoliceStationId(regFormPoliceStationId.getText());
        
        Date placement = new Date(regFormPlacementDate.getDate().getTime());
        regis.setDateOfBirth(placement);
        
        regis.setWeaponId(Integer.valueOf(regFormWeaponId.getText()));
        regis.setWeaponSerial(regFormWeaponSerial.getText());
        regis.setCourtId(Integer.valueOf(regFormCourtId.getText()));
        
        String courtType = regFormCourtType.getSelectedItem().toString();
        String courtName = regFormCourtName.getSelectedItem().toString();
        String courtCity = regFormCourtCity.getSelectedItem().toString();
        
        regis.setTribunalName(regFormTribunalName.getText());
        regis.setJudgeId(Integer.valueOf(regFormJudgeId.getText()));
        regis.setJudgeName(regFormJudgeName.getText());
        regis.setChiefJustice(regFormChiefJustice.getText());
        regis.setPpId(Integer.valueOf(regFormPPId.getText()));
        regis.setPpName(regFormPPName.getText());
        regis.setJailId(Integer.valueOf(regFormJailId.getText()));
        
        String jailType = regFormJailType.getSelectedItem().toString();
        regis.setJailName(regFormJailName.getText());
        String jailCity = regFormJailCity.getSelectedItem().toString();
        
        regis.setUserId(Integer.valueOf(regFormUserId.getText()));
        regis.setPassword(regFormPassword.getText());
        
        int status = regDAO.save(regis);
        
        if(status > 0) {
            JOptionPane.showMessageDialog(rootPane, "Registration Confirmed!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Registration Failed!");
        }
    }//GEN-LAST:event_btnRegFormSubmitActionPerformed

